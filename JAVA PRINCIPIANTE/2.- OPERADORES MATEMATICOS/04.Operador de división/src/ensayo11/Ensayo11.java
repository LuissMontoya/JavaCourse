package ensayo11;
public class Ensayo11 {
    public static void main(String[] args) {
        float a,b,c;//tomar en cuanta que el asignamiento de la varible afecta a  los valores, en éste caso se usa unavariable de decimal
        a=2;
        b=3;
        c=a/b;//hay dos diviciones, una es la que se considera div, y ésta nos da el resultado de la divición
        System.out.println(c);
        
        int e,f,g;
        e=2;
        f=3;
        g=e%f;//la otra se considera mod, la cual nos da el residuo de la divición.
        System.out.println(g);
    }
    
}
